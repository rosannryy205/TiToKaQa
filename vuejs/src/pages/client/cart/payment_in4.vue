<script>
import {
  onMounted,
  computed,
  ref,
  watch
} from 'vue'
import { useRouter } from 'vue-router'
import numeral from 'numeral'
import axios from 'axios'
import dayjs from 'dayjs'
import { toast } from 'vue3-toastify'

import { FoodList } from '@/stores/food'
import { Discounts } from '@/stores/discount'
import { Cart } from '@/stores/cart'
import { User } from '@/stores/user'
import { useShippingStore } from '@/stores/shippingStore'
import { storeToRefs } from 'pinia'

const shippingStore = useShippingStore()
const { shippingFee } = storeToRefs(shippingStore)

export default {
  //==============
  // METHODS FORMAT
  //==============
  methods: {
    formatNumber(value) {
      return numeral(value).format('0,0')
    },
    getImageUrl(image) {
      return `/img/food/${image}`
    },
  },
  setup() {
    const router = useRouter()

    //==============
    // STATE & REF
    //==============
    const note = ref('')
    const paymentMethod = ref('')
    const activeTab = ref("T·∫•t c·∫£ m√£")
    const showAllVoucher = ref(false)
    const today = dayjs().format('YYYY-MM-DD')

    //==============
    // STORE
    //==============
    const { user, form } = User.setup()
    const { cartItems, cartKey } = Cart()
    const { isLoading } = FoodList.setup()

    const {
      discounts,
      discountInput,
      selectedDiscount,
      discountId,
      totalPrice,
      getAllDiscount,
      discountInputId,
      showMoreDiscounts,
      applyDiscountCode,
      handleDiscountInput,
      finalTotal,
      discountFoodAmount,
      totalQuantity,
      totalPriceItem,
      loadCart,
      discountShipAmount,
      getAllChildCategoryIds,
      fetchUserDiscounts,
      userDiscounts
    } = Discounts()

    //==============
    // TAB DISCOUNT
    //==============
    const tabs = ["T·∫•t c·∫£ m√£", "M√£ c·ªßa t√¥i"]
    const setActive = (tab) => {
      activeTab.value = tab
    }
    const displayedDiscounts = computed(() => {
      if (activeTab.value === "T·∫•t c·∫£ m√£") return discountsFiltered.value
      if (activeTab.value === "M√£ c·ªßa t√¥i") return userDiscounts.value
      return []
    })

    const removeDiscountCode = () => {
      selectedDiscount.value = null
    }

    const discountsFiltered = computed(() => {
      return discounts.value.filter((discount) => {
        const endDate = dayjs(discount.end_date).format('YYYY-MM-DD')
        return discount.used < discount.usage_limit && endDate >= today
      })
    })

    const isLoggedIn = computed(() => !!localStorage.getItem('token'))

    //==============
    // ORDER & PAYMENT
    //==============
    const check_out = async (orderId) => {
      try {
        if (!paymentMethod.value) return toast.error('Vui l√≤ng ch·ªçn ph∆∞∆°ng th·ª©c thanh to√°n.')

        if (paymentMethod.value === 'VNPAY') {
          if (!orderId || finalTotal.value <= 0)
            return toast.error('Th√¥ng tin ƒë∆°n h√†ng ho·∫∑c s·ªë ti·ªÅn kh√¥ng h·ª£p l·ªá ƒë·ªÉ thanh to√°n VNPAY.')

          const res = await axios.post('http://127.0.0.1:8000/api/payments/vnpay-init', {
            order_id: orderId,
            amount: finalTotal.value,
          })

          if (res.data?.payment_url) {
            localStorage.setItem('payment_method', paymentMethod.value)
            localStorage.removeItem(cartKey.value)
            window.location.href = res.data.payment_url
          } else {
            toast.error('Kh√¥ng t·∫°o ƒë∆∞·ª£c link thanh to√°n VNPAY.')
          }
          return
        }

        if (paymentMethod.value === 'MOMO') {
          toast.info('Ch·ª©c nƒÉng thanh to√°n MoMo ƒëang ƒë∆∞·ª£c ph√°t tri·ªÉn!')
          localStorage.setItem('payment_method', paymentMethod.value)
          localStorage.removeItem(cartKey.value)
          return
        }

        if (paymentMethod.value === 'COD') {
          if (user.value?.status === 'Block') {
            toast.error('T√†i kho·∫£n c·ªßa b·∫°n ƒë√£ b·ªã h·∫°n ch·∫ø. Kh√¥ng th·ªÉ thanh to√°n b·∫±ng ti·ªÅn m·∫∑t.');
            return;
          }
          await axios.post('http://127.0.0.1:8000/api/payments/cod-payment', {
            order_id: orderId,
            amount_paid: finalTotal.value,
          })
          localStorage.setItem('payment_method', paymentMethod.value)
          localStorage.removeItem(cartKey.value)
          toast.success('ƒê·∫∑t h√†ng v√† thanh to√°n b·∫±ng ti·ªÅn m·∫∑t th√†nh c√¥ng!')
          router.push('/payment-result')
        }
      } catch (error) {
        console.error(error)
        toast.error(error?.response?.data?.message || 'Thanh to√°n th·∫•t b·∫°i!')
      }
    }

    const submitOrder = async () => {
      isLoading.value = true
      try {
        if (!selectedProvince.value || !selectedDistrict.value || !selectedWard.value) {
          alert('Vui l√≤ng ch·ªçn ƒë·∫ßy ƒë·ªß ƒë·ªãa ch·ªâ.')
          isLoading.value = false
          return
        }

        const province = provinces.value.find(p => p.ProvinceID === selectedProvince.value)
        const district = districts.value.find(d => d.DistrictID === selectedDistrict.value)
        const ward = wards.value.find(w => w.WardCode === selectedWard.value)

        const fullAddress = `${form.value.address}, ${ward?.WardName}, ${district?.DistrictName}, ${province?.ProvinceName}`

        if (!fullAddress || cartItems.value.length === 0) {
          toast.error('Vui l√≤ng nh·∫≠p ƒë·ªãa ch·ªâ v√† ch·ªçn m√≥n ƒÉn.')
          isLoading.value = false
          return
        }

        const orderData = {
          user_id: user.value?.id || null,
          guest_name: form.value.fullname,
          guest_email: form.value.email,
          guest_phone: form.value.phone,
          guest_address: fullAddress,
          note: note.value || '',
          total_price: finalTotal.value || 0,
          shippingFee: shippingFee.value || 0,
          money_reduce: discountFoodAmount.value > 0 ? discountFoodAmount.value : discountShipAmount.value,
          discount_id: discountId.value || null,
          order_detail: cartItems.value.map(item => ({
            food_id: item.type === 'Food' ? item.id : null,
            combo_id: item.type === 'Combo' ? item.id : null,
            quantity: item.quantity,
            price: item.price,
            type: item.type,
            toppings: item.toppings.map(t => ({
              food_toppings_id: t.food_toppings_id,
              price: t.price
            }))
          }))
        }

        const res = await axios.post('http://127.0.0.1:8000/api/order', orderData)

        if (res.data?.status && res.data.order_id) {
          toast.success('ƒê∆°n h√†ng ƒë√£ ƒë∆∞·ª£c t·∫°o th√†nh c√¥ng!')
          if (orderData.discount_id) {
            await axios.post('http://127.0.0.1:8000/api/discounts/use', {
              discount_id: orderData.discount_id,
              order_id: res.data.order_id,
            })
          }
          await check_out(res.data.order_id)
        } else {
          toast.error('Kh√¥ng nh·∫≠n ƒë∆∞·ª£c ID ƒë∆°n h√†ng t·ª´ server.')
        }
      } catch (err) {
        console.error(err)
        toast.error(err?.response?.data?.message || 'ƒê·∫∑t h√†ng th·∫•t b·∫°i.')
      } finally {
        isLoading.value = false
      }
    }

    //==============
    // GHN - ƒê·ªäA CH·ªà & V·∫¨N CHUY·ªÇN
    //==============
    const provinces = ref([])
    const districts = ref([])
    const wards = ref([])

    const selectedProvince = ref('')
    const selectedDistrict = ref('')
    const selectedWard = ref('')
    const shippingServices = ref([])
    const selectedService = ref(null)
    const ghnToken = 'ce7a164e-3e1c-11f0-a700-860cdd37d888'
    const hasShippingFee = computed(() => shippingFee.value > 0)

    const fetchProvinces = async () => {
      try {
        const res = await axios.get('https://online-gateway.ghn.vn/shiip/public-api/master-data/province', {
          headers: { Token: ghnToken }
        })
        provinces.value = res.data.data
        const hcm = provinces.value.find(p => p.ProvinceName.toLowerCase().includes('h·ªì ch√≠ minh'))
        if (hcm) {
          selectedProvince.value = hcm.ProvinceID
          fetchDistricts()
        }
      } catch (err) {
        console.error('fetchProvinces error:', err)
      }
    }

    const fetchDistricts = async () => {
      shippingServices.value = []
      selectedService.value = null
      selectedDistrict.value = ''
      selectedWard.value = ''
      districts.value = []
      wards.value = []

      try {
        const res = await axios.post('https://online-gateway.ghn.vn/shiip/public-api/master-data/district',
          { province_id: selectedProvince.value },
          { headers: { Token: ghnToken } }
        )
        districts.value = res.data.data
      } catch (err) {
        console.error('fetchDistricts error:', err)
      }

      watch(selectedDistrict, (newVal) => {
        if (newVal) fetchShippingServices()
      })
    }

    const fetchWards = async () => {
      selectedWard.value = ''
      wards.value = []

      try {
        const res = await axios.post('https://online-gateway.ghn.vn/shiip/public-api/master-data/ward',
          { district_id: selectedDistrict.value },
          { headers: { Token: ghnToken } }
        )
        wards.value = res.data.data
      } catch (err) {
        console.error('fetchWards error:', err)
      }
    }

    const fetchShippingServices = async () => {
      if (!selectedDistrict.value) return
      try {
        const res = await axios.post('http://localhost:8000/api/ghn/service', {
          to_district_id: selectedDistrict.value,
        })
        shippingServices.value = res.data || []
        selectedService.value = shippingServices.value[0] || null
      } catch (err) {
        console.error('fetchShippingServices error:', err)
      }
    }

    //==============
    // LIFECYCLE
    //==============
    onMounted(async () => {
      await getAllDiscount({ source: 'system' })
      await fetchUserDiscounts()
      loadCart()
      fetchProvinces()
    })

    watch([selectedDistrict, selectedWard, selectedService], () => {
      if (selectedDistrict.value && selectedWard.value && selectedService.value) {
        shippingStore.calculateShippingFee({
          toDistrictId: selectedDistrict.value,
          toWardCode: selectedWard.value,
          serviceId: selectedService.value.service_id,
          insuranceValue: finalTotal.value || 0,
        })
      }
    })

    //==============
    // RETURN
    //==============
    return {
      user,
      form,
      note,
      tabs,
      setActive,
      activeTab,
      displayedDiscounts,
      showAllVoucher,
      isLoggedIn,
      paymentMethod,
      check_out,
      submitOrder,
      isLoading,
      cartItems,
      cartKey,
      totalPrice,
      totalQuantity,
      totalPriceItem,
      finalTotal,

      discounts,
      discountInput,
      selectedDiscount,
      discountId,
      discountInputId,
      showMoreDiscounts,
      applyDiscountCode,
      handleDiscountInput,
      discountFoodAmount,
      discountShipAmount,
      removeDiscountCode,
      discountsFiltered,
      today,
      getAllChildCategoryIds,
      userDiscounts,

      provinces,
      districts,
      wards,
      selectedProvince,
      selectedDistrict,
      selectedWard,
      ghnToken,
      fetchDistricts,
      fetchWards,
      shippingServices,
      selectedService,
      shippingFee,
      hasShippingFee
    }
  }
}
</script>


